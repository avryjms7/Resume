# grade converter

grade = int(input("Enter Student's Grade from 0-100?"))

if grade >= 90:
    print("A")
elif grade >= 80 and grade <= 89:
    print("B")
elif grade >= 70 and grade <= 79:
    print("C")
elif grade >= 65 and grade <= 69:
    print("D")
else:
    print("F")


                
                
